  <h2> Shortcode Parameter</h2>
</div>

<div class="postdesigns">
  <p><strong>limit:</strong> limit="3" limitize the faq items by default all item will be displays</p>
  <p><strong>category: </strong>category="category id" FAQs can be categories by category id </p>
  <p><strong>morethenoneopen:</strong> morethenoneopen="true" if true all the faqs can be open while if false then only one faq at a time will open on click</p>
  <p><strong>transition_speed:</strong> transition_speed="300" transition speed when user click to open FAQ item by default 300</p>
  <p><strong>transition_type:</strong> transition_type="eases" A string indicating which easing function to use for the transition. ease(this is default)linear, ease-in, ease-out, ease-in-out.</p>
  <p><strong>design:</strong> FAQ plugin included with 8 color scheme you can select see in shortcode page.</p>
<h2>FAQ plugin can be customize design wiht the following parameter</h2>
  <p><strong>font_color:</strong> font_color="#000" you can change text color by using hexa color</p>
  <p><strong>border_color:</strong> border_color="#d9d9d9" faqs border color can be change by using hexa color</p>
  <p><strong>background_color:</strong> background_color="#f4f4f4"  faqs background color can be change by using hexa color</p>
  <p><strong>active_bg_color:</strong> active_bg_color="#fff"  open faqs color can be change with hexa color</p>
  <p><strong>active_title_color:</strong> active_title_color="#000" opened faqs title text color can be changed</p>
<h2>FAQ Plugin included icons</h2>
  <p><strong>icon_color:</strong> icon_color="black"  icon color can be select by white and black color</p>
  <p><strong>icon_position:</strong> icon_position="right" icon position can be select left or right</p>
  <p><strong>icon_type:</strong> icon_type="updown" icon type can be select by updown and plus</p>
